const normalizeBase = (value: string) =>
  value
    .trim()
    .toLowerCase()
    .replace(/ё/g, "е")
    .replace(/\./g, "")
    .replace(/\s*-\s*/g, "-")
    .replace(/\s+/g, " ");

export const normalizeCityInput = (value: string) => {
  const base = normalizeBase(value);
  return base.replace(/^город\s+/i, "").replace(/^г\s+/i, "");
};

export const normalizeNameInput = (value: string) => normalizeBase(value);

